export function getMedicationsByPatientId(patientId: number): Medication[] {
  return medications.filter((m) => m.patientId === patientId);
}

export function getMedicationById(id: number): Medication | undefined {
  return medications.find((m) => m.id === id);
}

export function getActiveMedications(patientId: number): Medication[] {
  return medications.filter((m) => m.patientId === patientId && m.status === 'Active');
}

export function getDiscontinuedMedications(patientId: number): Medication[] {
  return medications.filter((m) => m.patientId === patientId && m.status === 'Discontinued');
}
